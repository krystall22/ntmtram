package main;

public class Exercise01 {

	public static void main(String[] args) {
		int myNum = 22;
		float myFloatNum = 3.443432f;
		char myLetter = 'T';
		boolean myBool = true;
		String myText = "Spring";
		
		System.out.printf("My Number is %d\n", myNum);
		System.out.printf("My Float Number is %.3f\n", myFloatNum);
		System.out.printf("My Letter is %c\n", myLetter);
		System.out.printf("My Boolean is %s\n", myBool);
		System.out.printf("My Text is %s\n", myText);
	}

}
